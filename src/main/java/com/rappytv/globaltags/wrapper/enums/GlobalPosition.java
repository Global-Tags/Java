package com.rappytv.globaltags.wrapper.enums;

/**
 * This enum represents all possible positions in which a tag can be displayed relative to the player's real nametag position
 */
public enum GlobalPosition {
    /**
     * The tag is above the nametag
     */
    ABOVE,
    /**
     * The tag is below the nametag
     */
    BELOW,
    /**
     * The tag is right next to the nametag
     */
    RIGHT,
    /**
     * The tag is left next to the nametag
     */
    LEFT
}
